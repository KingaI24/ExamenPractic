package Domain;

public class IdDays {

    private int id, noDays;

    public IdDays(int id, int noDays) {
        this.id = id;
        this.noDays = noDays;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNoDays() {
        return noDays;
    }

    public void setNoDays(int noDays) {
        this.noDays = noDays;
    }

}
